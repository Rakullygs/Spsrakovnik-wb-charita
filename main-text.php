                    <h2>ONE MORE DAY FOR CHILDREN</h2>
                    
                    Náš nadační fond ONE MORE DAY FOR CHILDREN má krom 148 adoptivních rodičů na dálku také další plány. Rozšířit již postavený dětský domov na severu Keni v oblasti Laikipia poblíž města Doldol, kde k dnešku již žije 64 našich adoptivní děti, které již nikoho nemají.
                    <br> <br>
Dalšími plány jsou – Pomoci smutným případům dětí ze slumů poblíž města Nanyuki, které jsou buďto HIV pozitivní od narození, jelikož pozitivní krev jim předala matka, nebo jim rodiče zemřeli na AIDS a nemá se o ně kdo starat. Tudíž nemohou navštěvovat školu, strádají hlady, spí na jedné posteli bez matrace spolu s dalšími 5 sourozenci a mohl bych mluvit a psát ještě dlouho. Dále také masajské dívenky zachránit od manželství a pomoci jim formou adopce na dálku, díky které budou moci chodit do školy. Chceme pro ně sehnat adoptivní rodiče na dálku a umístit k nám do nově postaveného dětského domova.
<br> <br>
ZALOŽENÍ Nadačního fondu: Dne 3.4. 2009 jsem s pomocí Hellen Gathogo z Keni založil charitativní organizaci ve které působím od 16.8. 2010 jako president. V Keni s názvem „ONE MORE DAY FOR CHILDREN“ a v Čechách nadační fond se stejným názvem. Tato organizace pomáhá dětem v oblasti národního parku Mount Kenya , oblasti Laikipia a provincie Doldol v Keni.
<br> <br>
Děti v této oblasti jsou velmi vyhladovělé, nemocné a pozitivní na HIV. Ve Slumech kolem města Nanyuki lze občas vidět děti na ulici. Jsou bez domova a bez rodičů, čichají lepidlo aby neměli hlad. Někdy jsou vyhozeny z domova, jelikož je pěstouni nemají jak uživit. Proto jsem se rozhodl zřídit tuto organizaci a postavit pro tyto děti dětský domov.

