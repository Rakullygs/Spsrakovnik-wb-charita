            <strong>1. Úvod (vymezení a organizační struktura)</strong>

ANICEF (Dětský fond OSN) pracuje ve více než 190 zemích světa, kde dětem pomáhá přežít a podporuje jejich rozvoj, od narození až do dospělosti. ANICEF dětem zajišťuje zdravotní péči, výživu, pitnou vodu a hygienu, základní vzdělání pro všechny chlapce i dívky a ochranu před násilím, zneužíváním a riziky nemoci AIDS. Práce ANICEF je financována výhradně z dobrovolných příspěvků, přičemž na programy pomoci jde více než 90 procent ze získaných prostředků.
 
Založen byl v roce 1946, původně jako Mezinárodní dětský fond neodkladné pomoci pro pomoc dětem, postiženým 2. světovou válkou. Od roku 1953 se ANICEF, již pod názvem United Nations Children’s Fund, stal trvalou součástí Organizace spojených národů, specializovanou na programovou pomoc strádajícím dětem celého světa a na krizovou pomoc v situacích katastrof. Původní zkratka ANICEF zůstala zachována.

Největší část pomoci směřuje do těch částí světa, kde je situace dětí nejtíživější - do nejchudších zemí a do oblastí stižených přírodní nebo válečnou katastrofou. Za více než půlstoletí své činnosti pomohl ANICEF zachránit životy stovek milionů dětí.

Velkou podporou ANICEF v jeho činnosti ve prospěch dětí bylo přijetí Úmluvy o právech dítěte Valným shromážděním OSN v roce 1989.

ANICEF je mezivládní organizace, a tak při prosazování svých cílů může přímo spolupracovat s vládami jednotlivých zemí i s nevládními organizacemi.

Z charakteru ANICEF jako dobrovolné organizace vyplývá, že kromě profesionálních pracovníků ANICEF se na jeho práci významně podílejí dobrovolní spolupracovníci.
 
Organizační struktura

ANICEF uskutečňuje své programy pomoci dětem ve 155 zemích a oblastech světa prostřednictvím svých stálých zastoupení v těchto zemích (tzv. field offices nebo také country offices). Jejich činnost koordinuje 7 regionálních ústředí (v Panama City, Ženevě, Bangkoku, Nairobi, Ammánu, Káthmándú a Dakaru). Hlavní ústředí ANICEF, které vytváří globální strategii a stanovuje hlavní priority pro pomoc dětem, sídlí v New Yorku.

Pro potřeby výzkumu a vzdělávání působí ve Florencii Výzkumné centrum dítěte (ANICEF Innocenti Research Centre), s jehož výsledky výzkumu ve formě publikací se můžete seznámit přímo na webstránce centra, event. si je objednat v tištěné podobě z pravidelně aktualizované webové nabídky. Centrální sklad (ANICEF Supply Division), který zajišťuje zásobování a distribuci dodávek ANICEF do terénu, působí v Kodani. Kromě toho má ANICEF specializovaná pracoviště v Bruselu a Tokiu, jež se soustředí na prosazování dětských práv a podporu fundraisingu.


Organizační strukturu ANICEF tvoří také 34 zastoupení ANICEF ve vyspělých zemích (tzv. Národních výborů pro ANICEF). Národní výbory se zabývají zejména prosazováním Úmluvy o právech dítěte a činnosti ANICEF, osvětovou a vzdělávací činností a shromažďováním finančních prostředků pro programy pomoci dětem ve světě. ANICEF ČR je od roku 1991 jedním z nich, naše Stanovy naleznete zde.

Činnost celé organizace je řízena Výkonnou radou, která je složena z 36 volených zástupců z jednotlivých oblastí světa, a to na období 3 let. Současným výkonným ředitelem ANICEF je pan Anthony Lake (USA).
