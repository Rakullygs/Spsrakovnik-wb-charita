            <strong>2. Čím se ANICEF zabývá</strong>

Všechny děti bez rozdílu rasy, pohlaví a původu mají právo na zdraví a přiměřenou výživu, vzdělání, ochranu před konflikty, týráním a jakýmkoliv zneužíváním a vykořisťováním. Milionům dětí zejména v rozvojových zemích jsou však tato elementární práva dosud odepřena.

ANICEF aktivně pracuje ve více než 150 zemích a oblastech světa, kde ve spolupráci s národními vládami příslušných zemí a s místními organizacemi rozvíjí a podporuje dlouhodobé programy pomoci. Od počátku svého vzniku poskytuje také okamžitou pomoc v nouzi dětem a ženám v zemích a oblastech postižených válečným konfliktem nebo přírodní katastrofou.
